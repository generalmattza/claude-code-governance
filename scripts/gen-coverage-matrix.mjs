#!/usr/bin/env node
/**
 * gen-coverage-matrix.mjs - Auto-generate the threat-to-hook coverage matrix.
 *
 * Walks packages/hooks/src/*\/index.ts, dynamically imports each hook's
 * compiled module from packages/hooks/dist/<name>/index.js, reads its
 * manifest, groups by threat ID, and writes docs/coverage-matrix.md.
 *
 * Profiles are rendered with effective severity per hook (e.g.
 * "baseline=warn, strict=block, regulated=block").
 *
 * Usage: node scripts/gen-coverage-matrix.mjs
 */

import { readdirSync, writeFileSync, existsSync, readFileSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { dirname, join, relative, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const SRC_DIR = join(REPO_ROOT, 'packages/hooks/src');
const DIST_DIR = join(REPO_ROOT, 'packages/hooks/dist');
const OUT_PATH = join(REPO_ROOT, 'docs/coverage-matrix.md');

function testInfoFor(hookName) {
  const testPath = join(SRC_DIR, hookName, `${hookName}.test.ts`);
  if (!existsSync(testPath)) {
    return { rel: null, count: 0 };
  }
  const body = readFileSync(testPath, 'utf8');
  // Count it() / test() invocations as a rough test-case count. Conservative
  // regex - only matches top-of-line invocations, no template strings.
  const matches = body.match(/^\s*(?:it|test)\s*\(\s*['"`]/gm) ?? [];
  return {
    rel: relative(REPO_ROOT, testPath),
    count: matches.length,
  };
}

function severityFor(severity, profile) {
  if (typeof severity === 'string') return severity;
  if (severity && typeof severity === 'object') return severity[profile] ?? '-';
  return '-';
}

function profileCell(manifest) {
  return manifest.profiles
    .map((p) => `${p}=${severityFor(manifest.severity, p)}`)
    .join(', ');
}

async function loadManifests() {
  const hookDirs = readdirSync(SRC_DIR, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort();

  const manifests = [];
  for (const name of hookDirs) {
    const distEntry = join(DIST_DIR, name, 'index.js');
    if (!existsSync(distEntry)) {
      throw new Error(`missing dist entry for ${name} at ${distEntry} (run pnpm build)`);
    }
    const mod = await import(pathToFileURL(distEntry).href);
    if (!mod.default?.manifest) {
      throw new Error(`${name}: dist module has no default.manifest`);
    }
    manifests.push(mod.default.manifest);
  }
  return manifests;
}

function groupByThreat(manifests) {
  const map = new Map();
  for (const m of manifests) {
    const list = map.get(m.threat) ?? [];
    list.push(m);
    map.set(m.threat, list);
  }
  return [...map.entries()].sort(([a], [b]) => a.localeCompare(b));
}

function buildPage(manifests) {
  const grouped = groupByThreat(manifests);

  let body = `# Coverage Matrix

> Auto-generated by \`scripts/gen-coverage-matrix.mjs\` from hook manifests. Do not hand-edit. Run \`pnpm gen:coverage-matrix\` to regenerate.
>
> Each row groups hooks by their declared \`threat\` field. The Profiles column shows the effective severity per profile, derived from each hook's \`severity\` field (scalar or per-profile object).

## Threat to hook map

| Threat | Hooks | Profiles |
| --- | --- | --- |
`;
  for (const [threat, hooks] of grouped) {
    const hookNames = hooks
      .map((h) => `[\`${h.name}\`](hooks/${h.name}.md)`)
      .join(', ');
    const profilesByHook = hooks
      .map((h) => `${h.name}: ${profileCell(h)}`)
      .join('; ');
    body += `| \`${threat}\` | ${hookNames} | ${profilesByHook} |\n`;
  }

  // Per-event listing.
  body += `\n## Hooks by event\n\n`;
  const byEvent = new Map();
  for (const m of manifests) {
    const list = byEvent.get(m.event) ?? [];
    list.push(m);
    byEvent.set(m.event, list);
  }
  const events = [...byEvent.keys()].sort();
  for (const ev of events) {
    body += `### ${ev}\n\n`;
    body += `| Hook | Matchers | Threat | Severity |\n| --- | --- | --- | --- |\n`;
    for (const m of byEvent.get(ev).sort((a, b) => a.name.localeCompare(b.name))) {
      const matchers = m.matchers.length > 0 ? m.matchers.map((x) => `\`${x}\``).join(', ') : '(none)';
      const sev =
        typeof m.severity === 'string'
          ? m.severity
          : Object.entries(m.severity)
              .map(([p, s]) => `${p}=${s}`)
              .join(', ');
      body += `| [\`${m.name}\`](hooks/${m.name}.md) | ${matchers} | \`${m.threat}\` | ${sev} |\n`;
    }
    body += `\n`;
  }

  // Threat-to-test mapping.
  body += `## Threat to test map\n\n`;
  body += `Each hook ships with a companion vitest suite at \`packages/hooks/src/<name>/<name>.test.ts\`. The cases counted below are top-of-line \`it()\` / \`test()\` invocations; assertions inside helpers are not counted.\n\n`;
  body += `| Threat | Hook | Tests | Cases |\n| --- | --- | --- | --- |\n`;
  let totalCases = 0;
  let hooksWithTests = 0;
  for (const [threat, hooks] of grouped) {
    for (const h of hooks.sort((a, b) => a.name.localeCompare(b.name))) {
      const info = testInfoFor(h.name);
      if (info.rel) {
        hooksWithTests += 1;
        totalCases += info.count;
        body += `| \`${threat}\` | [\`${h.name}\`](hooks/${h.name}.md) | [\`${info.rel}\`](../${info.rel}) | ${info.count} |\n`;
      } else {
        body += `| \`${threat}\` | [\`${h.name}\`](hooks/${h.name}.md) | _missing_ | 0 |\n`;
      }
    }
  }

  // Summary stats.
  const totalHooks = manifests.length;
  const totalThreats = grouped.length;
  body += `\n## Summary\n\n`;
  body += `- Total hooks: ${totalHooks}\n`;
  body += `- Distinct threats covered: ${totalThreats}\n`;
  body += `- Events: ${events.join(', ')}\n`;
  body += `- Hooks with a companion test file: ${hooksWithTests} / ${totalHooks}\n`;
  body += `- Total hook test cases (rough): ${totalCases}\n`;

  body += `\n---\n\n_Generated by \`scripts/gen-coverage-matrix.mjs\`. See [\`docs/threat-model.md\`](./threat-model.md) for full threat descriptions and [\`docs/known-bypasses.md\`](./known-bypasses.md) for documented gaps._\n`;

  return body;
}

async function main() {
  const manifests = await loadManifests();
  const page = buildPage(manifests);
  writeFileSync(OUT_PATH, page);
  process.stdout.write(`gen-coverage-matrix: wrote ${OUT_PATH} (${manifests.length} hooks)\n`);
}

main().catch((err) => {
  process.stderr.write(`gen-coverage-matrix failed: ${err.stack ?? err.message}\n`);
  process.exit(1);
});
